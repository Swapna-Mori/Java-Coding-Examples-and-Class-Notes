package operatorsdemo;

public class ArithmeticOperators {
	public static void main(String[] args) {
		
		float fvalue = 23.5f;
		float fvalue1 = 45.345f;
		System.out.println("Addition =" + (fvalue1 + fvalue));

		System.out.println("division =" + (fvalue / fvalue1));
		

		System.out.println("Substraction =" + (fvalue1 - fvalue));
		

		System.out.println("Multiplication =" + (fvalue1 * fvalue));
		
	
		System.out.println("Modulus =" + (fvalue1 % fvalue));
		

		
	}
}
